<?php

/* OPERADORES DE INCREMENTO E DECREMENTO

* SERVEM PARA SOMAR OU SUBTRAIR EM (+1 OU -1)
* QUALQUER VALOR DO TIPO NÚMERICO OU STRING

*/

$valorInc = 20;
$valorDec = 20;

// Incremento:
// Pré-incremento
echo ++$valorInc;
echo '<hr>';

// Pós-incremento
echo $valorInc++;
echo $valorInc;
echo '<hr>';

// Decremento:
// Pré-decremento
echo --$valorDec;
echo '<hr>';

// Pós-decremento
echo $valorDec--;
echo $valorDec;
echo '<hr>';

?>